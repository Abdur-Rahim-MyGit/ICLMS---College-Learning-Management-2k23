            1860 and started Islamiah Elementary School in 1903.The founding fathers of the Society were Janab T.Hajee
            Badruddin Sahib, Janab Malang Hayath Batcha Sahib, Janab T. Ameenuddin Sahib and Janab Malang Ahmed Batcha
            Sahib. The full-fledged Islamiah High School came into existence in 1912. In 1916, the Society resolved to
            establish Islamiah College and the foundation stone of the College was laid by His Excellency Lord Pentland,
            the then Governer of Madras Presidency. With princely donations from the Muslim philanthropists, the College
            was started in 1919 and got recognition from July 1921. The building in which the College is housed at
            present was opened by Nawab C.Abdul Hakeem Sahib who gave a munificent donation of Rs. 50,000 to the
            Society.</br>
